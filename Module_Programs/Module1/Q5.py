#Usage of mathematical functions in python like math.ceil, floor, fabs, fmod, trunc, pow, sqrt etc.
#importing math module
import math
#using math.ceil
sample_num=123.456
print(math.ceil(sample_num))
#using math.floor
sample_num=123.456
print(math.floor(sample_num))
#using math.fabs
sample_num=-123.456
print(math.fabs(sample_num))
#using math.mod
sample_num=123.456
print(math.modf(sample_num))
#using math.trunc
sample_num=123.456
print(math.trunc(sample_num))